<div style="margin-top:2%; font-size:30px; line-height:45px;"> <b>Remember</b></div>
Jesus certainly understands all that we are going through because being born fully man; He was “in all points tempted as we are”. He endured his sufferings for our sakes so that we can have His peace and anointing to rise above the troubles we are facing!
 

