<div style="font-size:25px; color:rgb(87, 87, 160); margin-top:2%;">SCRIPTURE: “For we do not have a high priest who cannot sympathize with our weaknesses, but was in all points tempted as we are, yet without sin.”<br/>
— Hebrew 4 vs 15.</div>

<div style="line-height:35px; margin-top:5%; font-size:20px;">
Do you sometimes wonder if God really knows what it is like to be poor, rejected by people or sick with aches and pains. Perhaps you are asking, “Does God really understand what I am going through?”
My friend, Jesus was no stranger to emotional or physical pains, poverty or hardships. He was born into a poor family. He had a smelly manger for His birthplace (see Luke 2: 7). From an early age, as a carpenter, He knew all about working hard with His hands, standing on His feet all day long and returning home with aches in His body.
The religious leaders of His day made life difficult for Him. They challenged His authority (see Mathew 21 vs 23) and tested His teachings (see Mathew 19 vs 3) they also called Him a glutton, winebibber, friend of tax collectors and sinners (see Mathew 11 vs 19) and blasphemer (see Mark 2 vs 7).
They said that he was demon-possessed and mad (see John 10 vs 20), put Him on the spot when they brought an adulterous woman to Him (see John 8 vs 2 – 11), attempted to stone Him (see John 8 vs 59, 10:31 – 39) and accused Him of perverting the nation (see Luke 23 vs 2).
Have you been chased out of your home because of your Christian beliefs? Jesus understands what you are going through. He was chased out of His home town (see Luke 4 vs 29). Have you been rejected by someone who love? Jesus also experienced the pain of being denied by a loved one (see Luke 22: 54 – 62) and betrayed by one considered close to Him (see Luke 22 vs 47 – 48). He also knows all about the sickness you are suffering because He bore your sickness and pain on the cross (see Isaiah 53 vs 4).
<br/>


