<div style="font-size:25px; color:rgb(87, 87, 160); margin-top:2%;">SCRIPTURE:"Christ has redeemed us from the curse of the law, having become a curse for us (for it is written, cursed is everyone who hangs on a tree)".<br/>
—Galatians 3 vs 13 </div>

<div style="line-height:35px; margin-top:5%; font-size:20px;">
Many believers probably know that Jesus died; our sins were forgiven because He shed His blood. Without the shedding of blood, there is no forgiveness of sins (see Ephesians 1 vs 7, Hebrews 9 vs 22). But why did Jesus have to die on the cross, since the capital punishment of Israel during the time of Christ was stoning and crucifixion?<br/>
Jesus hung and died on the cross because He knew the law which says that “he who is hanged is accused of God (Deuteronomy 21 vs 23). He wanted to redeem us from every curse of the land, so He went to the cross, “having become a curse for us”.<br/>
Jesus took all our curses at the cross, so that we can take all His blessings!<br/>
The moment you received Jesus, every curse in your life was destroyed.<br/>
And the way you experience this is to simply believe and confess what Jesus has done for you. Say,<br/>
“Because of Jesus, I am blessed.”<br/>
The more you say it and believe it, the more you will experience it.<br/>
So if there is an area in your life in which you feel oppressed, for example, a skin condition that refuses to heal, say, “Christ has redeemed me from this skin condition. I refuse it and reject it in Jesus’ name because by His stripes I am healed” (see 1 Peter 2 vs 24)!<br/>
Believe it and say it until you see it!<br/>
“But Pastor Oluwalana, my father died of cancer, his father died of cancer and I have been told by my doctor that there is a likelihood that I might get it too. It’s in our blood”.<br/>
My friend, the blood of Christ shed at the cross has redeemed you from all curses, including diseases like cancer! God put the curse of cancer on Jesus at the cross. And He put all the blessings of Jesus on you.<br/>
Therefore, expect only the blessings of Jesus to come upon you and overtake you!<br/>
 

